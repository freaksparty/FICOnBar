    private void btnAceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptActionPerformed
        if(txtName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe introducir un nombre","Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try{
            Util.addProductTypeToDB(txtName.getText());
            Util.registerLogToDB("AÃ±adido tipo de producto " + txtName.getText(), User.user.getUserId());            
        }catch(HibernateException hEx){
            Util.registerLogToDB(hEx.getCause().getMessage(), User.user.getUserId());
            JOptionPane.showMessageDialog(this, "Se ha producido un error. Contacte a un administrador.","Error", JOptionPane.ERROR_MESSAGE);
            if(numTypesAdded == 0)
                cancel = true;
            this.dispose();
        }
        numTypesAdded++;
    }//GEN-LAST:event_btnAceptActionPerformed

