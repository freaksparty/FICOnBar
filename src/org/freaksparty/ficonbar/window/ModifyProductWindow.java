    private void initCombos(){
        cmbProdType.removeAllItems();
        cmbNewType.removeAllItems();
        cmbComplementOf.removeAllItems();
        cmbProdType.addItem("-");
        cmbNewType.addItem("-");
        cmbComplementOf.addItem("-");
        List<ProductType> typeList = Util.getProductTypeAll();
        for(ProductType pt:typeList){
            cmbProdType.addItem(pt.getName());
            cmbNewType.addItem(pt.getName());
            cmbComplementOf.addItem(pt.getName());
        }
    }
    
    private void resetData(){
        txtBarcode.setText("");
        txtBarcode.setEnabled(false);
        txtName.setText("");
        txtName.setEnabled(false);
        initCombos();
        cmbComplementOf.setEnabled(false);
        cmbNewType.setEnabled(false); 
    }
    
    private boolean hasChanges(String name, String barcode, int complementOf, int newType){
        return !product.getName().equals(name) || 
                !product.getBarcode().equals(barcode) ||
                (product.getProductTypeByProductType().getProductTypeId() != newType) ||
                (product.getProductTypeByComplementOf().getProductTypeId() != complementOf);
    }

