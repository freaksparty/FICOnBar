    private void btnAceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptActionPerformed
        if(cmbProdType.getSelectedIndex() == 0)
            JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de producto.","Warning",JOptionPane.WARNING_MESSAGE);
        
        if(cmbProduct.getSelectedIndex() == 0)
            JOptionPane.showMessageDialog(this, "Debe seleccionar un producto.","Warning",JOptionPane.WARNING_MESSAGE);
        
        if(!hasChanges(txtName.getText(), txtBarcode.getText(), cmbComplementOf.getSelectedIndex(), cmbNewType.getSelectedIndex()))
            JOptionPane.showMessageDialog(this, "Debe hacer algún cambio.","Warning",JOptionPane.WARNING_MESSAGE);
        try{
            String barcode = txtBarcode.getText() == null ? "" : txtBarcode.getText();
            Util.updatePoductInDB(productList.get(cmbProduct.getSelectedIndex() - 1).getProductId(), txtName.getText(), barcode, cmbNewType.getSelectedIndex(), cmbComplementOf.getSelectedIndex());
            Util.registerLogToDB("Modificado producto ", User.user.getUserId());
            resetData();
        }catch(HibernateException hEx){
            Util.registerLogToDB(hEx.getCause().getMessage(), User.user.getUserId());
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error, contacte a un administrador.","Error",JOptionPane.ERROR_MESSAGE);
            if(numProdModified == 0)
                cancel = true;
            this.dispose();
        }
        numProdModified++;
    }//GEN-LAST:event_btnAceptActionPerformed

    private void cmbProdTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProdTypeActionPerformed
        if(cmbProdType.getSelectedIndex() != 0){
            productList = Util.getProductByType(cmbProdType.getSelectedIndex());
            cmbProduct.removeAllItems();
            cmbProduct.addItem("-");            
            for(Product p:productList)
                cmbProduct.addItem(p.getName());
        }
    }//GEN-LAST:event_cmbProdTypeActionPerformed

    private void cmbProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProductActionPerformed
        if(cmbProduct.getSelectedIndex() > 0 && combosInitiated && productList.size() > 0){
            product = productList.get(cmbProduct.getSelectedIndex() - 1);
            txtName.setText(product.getName());
            txtName.setEnabled(true);
            txtBarcode.setText(product.getBarcode() == null ? "" : product.getBarcode());
            txtBarcode.setEnabled(true);
            cmbNewType.setSelectedIndex(product.getProductTypeByProductType().getProductTypeId());
            cmbNewType.setEnabled(true);
            cmbComplementOf.setSelectedIndex(product.getProductTypeByComplementOf() == null ? 0 : product.getProductTypeByComplementOf().getProductTypeId());
            cmbComplementOf.setEnabled(true);
        }
    }//GEN-LAST:event_cmbProductActionPerformed

