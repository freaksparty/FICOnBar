    private void initCombo() {
        List<ProductType> listProductTypes = Util.getProductTypeAll();
        cmbTypes.removeAllItems();
        cmbTypes.addItem("-");
        for(ProductType pt:listProductTypes){
            cmbTypes.addItem(pt.getName());
        }
    }
