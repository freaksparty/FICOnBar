    private void btnCloseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCloseActionPerformed
        
        if(numTypesDeleted == 0)
            cancel = true;
        this.dispose();
    }//GEN-LAST:event_btnCloseActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(cmbTypes.getSelectedIndex() != 0){
            String name = cmbTypes.getSelectedItem().toString();
            int confirmDel = JOptionPane.showConfirmDialog(this, "Desea eliminar el tipo de producto " + name + "?","Eliminar",JOptionPane.YES_NO_OPTION);
            if(confirmDel == JOptionPane.YES_OPTION){
                try{
                    Util.deleteProductTypeFromDB(name);
                    Util.registerLogToDB("Eliminado tipo de producto " + name, User.user.getUserId());
                }catch(HibernateException hEx){
                    Util.registerLogToDB(hEx.getCause().getMessage(), User.user.getUserId());
                    JOptionPane.showMessageDialog(this, "Se ha producido un error. Contacte a un administrador.","Error", JOptionPane.ERROR_MESSAGE);
                    if(numTypesDeleted == 0)
                        cancel = true;
                    this.dispose();
                }
                initCombo();
                numTypesDeleted++;                
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de producto.","Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

